             <p>
             L'eau est une ressource vitale et donc précieuse pour l'ensemble de la planète. L’accroissement de la population, le réchauffement climatique dû à l'activité́ humaine rend cet élément de plus en plus rare même dans les régions tempérées. 

Un être humain a besoin de 20 à 50 litres d’eau potable chaque jour pour satisfaire ses besoins. Aujourd’hui plus d’une personne sur six n’a pas accès à l’eau potable et chaque année, ce sont environ 1,5 millions d’enfants qui meurent de maladies liées à la consommation d’eau insalubre. Apprendre à protéger et à gérer durablement cette ressource précieuse est crucial pour l’avenir de notre planète.

